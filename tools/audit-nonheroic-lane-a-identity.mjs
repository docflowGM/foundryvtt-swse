#!/usr/bin/env node
/**
 * Lane A candidate identity audit for nonheroic weapon damage profiles.
 *
 * The bulk Lane A candidate generator (tools/generate-nonheroic-damage-
 * profile-candidates.mjs) and promotion tool (tools/promote-nonheroic-
 * damage-profile-candidates.mjs) originally identified a candidate row using
 * only a bare weapon-name marker (match.rawIncludes) and a bare actor+weapon
 * slug. Neither is unique when an actor has more than one printed attack row
 * for the same weapon name (a full-attack sequence with different attack
 * bonuses, or the same actor document appearing in more than one source
 * pack) -- both rows generate the identical marker and the identical
 * candidate slug, which the promotion tool's duplicate-marker/duplicate-slug
 * guards then correctly (but overly bluntly) refuse to promote together.
 * This left a large pool of otherwise-clean, already-classified "safe"
 * candidate rows unpromotable through any allowlist that named more than one
 * row per actor+weapon-name pair.
 *
 * This is a READ-ONLY audit. It does not promote any profile, does not
 * create any weapon item, does not touch actor packs, compendium packs, or
 * runtime code, and does not wire anything into the hydrator. It reads the
 * current staged Lane A "safe" candidate pool (already regenerated by
 * tools/generate-nonheroic-damage-profile-candidates.mjs with the
 * match.rawClause / disambiguated-slug identity fix) and reports how many
 * rows sit in duplicate actor+weapon-name groups, how those groups break
 * down (genuinely distinct printed rows vs. literal same-document
 * duplicates across source packs), and confirms every row in a genuinely
 * distinct group now has a unique rawClause marker and a unique slug.
 *
 * Inputs:
 *   - data/nonheroic/generated/nonheroic-weapon-damage-candidates.*.json
 *     (the current staged Lane A "safe" candidate pool)
 *
 * Outputs:
 *   - docs/audits/generated/nonheroic-lane-a-identity-audit.json
 *   - docs/audits/generated/nonheroic-lane-a-identity-audit.md
 */

import fs from 'node:fs';
import path from 'node:path';

const ROOT = process.cwd();
const CANDIDATE_DIR = 'data/nonheroic/generated';
const OUTPUT_JSON = 'docs/audits/generated/nonheroic-lane-a-identity-audit.json';
const OUTPUT_MD = 'docs/audits/generated/nonheroic-lane-a-identity-audit.md';

function readText(relPath) {
  return fs.readFileSync(path.join(ROOT, relPath), 'utf8');
}
function writeText(relPath, text) {
  fs.mkdirSync(path.dirname(path.join(ROOT, relPath)), { recursive: true });
  fs.writeFileSync(path.join(ROOT, relPath), text);
}
function exists(relPath) {
  return fs.existsSync(path.join(ROOT, relPath));
}
function clean(value) {
  return String(value ?? '').replace(/\s+/g, ' ').trim();
}

// Mirrors tools/generate-nonheroic-damage-profile-candidates.mjs
// normalizeWeaponName exactly, so grouping here matches grouping there.
function normalizeWeaponName(name) {
  return String(name || '')
    .toLowerCase()
    .normalize('NFKD')
    .replace(/[’']/g, '')
    .replace(/[^a-z0-9]+/g, ' ')
    .trim()
    .replace(/\s+/g, ' ');
}

function loadCandidatePool() {
  if (!exists(CANDIDATE_DIR)) return [];
  const files = fs.readdirSync(path.join(ROOT, CANDIDATE_DIR))
    .filter(f => /^nonheroic-weapon-damage-candidates\..*\.json$/.test(f))
    .sort();
  const rows = [];
  for (const file of files) {
    const relPath = path.join(CANDIDATE_DIR, file);
    const doc = JSON.parse(readText(relPath));
    for (const record of doc.records || []) {
      rows.push({ sourceFile: relPath, record });
    }
  }
  return rows;
}

function main() {
  const pool = loadCandidatePool();

  const groups = new Map();
  for (const { sourceFile, record } of pool) {
    const actorSlug = record.actor?.slugs?.[0] ?? null;
    const weaponName = record.weapon?.printedName ?? null;
    if (!actorSlug || !weaponName) continue;
    const key = `${actorSlug}::${normalizeWeaponName(weaponName)}`;
    if (!groups.has(key)) groups.set(key, []);
    groups.get(key).push({ sourceFile, record });
  }

  const singletonGroups = [];
  const duplicateGroups = [];
  for (const [key, members] of groups) {
    (members.length > 1 ? duplicateGroups : singletonGroups).push({ key, members });
  }

  // Classify each duplicate group by rawClause diversity:
  //   - identical: every member shares the exact same printed clause text
  //     (same weapon, same bonus, same formula). This is not a "different
  //     attack row" at all -- almost always the same underlying actor
  //     document appearing in more than one source pack (see
  //     crossPackSpanning below), or, less commonly, a genuinely repeated
  //     NPC statblock reused verbatim under the same name. No marker scheme
  //     keyed only on actor+weapon+bonus+formula can distinguish these from
  //     each other; only one representative should ever be promoted.
  //   - distinct: every member has a different rawClause (the intended
  //     "full-attack-sequence" case, e.g. a primary and iterative attack
  //     with the same weapon at different bonuses). The rawClause /
  //     disambiguated-slug fix makes every one of these independently
  //     promotable.
  //   - mixed: a combination of both within the same actor+weapon-name
  //     group.
  const classified = duplicateGroups.map(({ key, members }) => {
    const rawClauses = members.map(m => clean(m.record.match?.rawClause ?? '').toLowerCase());
    const uniqueClauses = new Set(rawClauses);
    const sourceFiles = new Set(members.map(m => m.sourceFile));
    let kind;
    if (uniqueClauses.size === 1) kind = 'identical';
    else if (uniqueClauses.size === members.length) kind = 'distinct';
    else kind = 'mixed';
    return {
      key,
      memberCount: members.length,
      kind,
      crossPackSpanning: sourceFiles.size > 1,
      sampleActorName: members[0].record.actor?.name ?? null,
      sampleWeaponName: members[0].record.weapon?.printedName ?? null,
      members: members.map(m => ({
        slug: m.record.slug,
        sourceFile: m.sourceFile,
        rawClause: m.record.match?.rawClause ?? null,
        rawIncludesMarker: m.record.match?.rawIncludes?.[0] ?? null,
        attackBonusText: m.record.printedAttack?.text ?? null,
        formulaPrinted: m.record.formula?.printed ?? null
      }))
    };
  });

  const identicalGroups = classified.filter(g => g.kind === 'identical');
  const distinctGroups = classified.filter(g => g.kind === 'distinct');
  const mixedGroups = classified.filter(g => g.kind === 'mixed');

  // Identity integrity check: match.rawClause is set directly from each
  // row's printed clause text, so two rows can only ever share a rawClause
  // marker if their printed content is genuinely identical (the "identical"
  // case, expected and documented above, not a bug). The meaningful check
  // is therefore on the *slug*: two rows with genuinely DIFFERENT printed
  // clause text must never end up with the same disambiguated slug, in any
  // group (including the distinct portion of a "mixed" group) -- that would
  // silently make one of them unpromotable via the promotion tool's
  // duplicate-slug guard. Rows that share both identical rawClause AND
  // identical slug are not counted here; that pairing is expected.
  const residualSlugCollisions = [];
  for (const g of classified) {
    const bySlug = new Map();
    for (const m of g.members) {
      if (!bySlug.has(m.slug)) bySlug.set(m.slug, []);
      bySlug.get(m.slug).push(m.rawClause);
    }
    for (const [slug, clauses] of bySlug) {
      const distinctClauses = new Set(clauses);
      if (distinctClauses.size > 1) {
        residualSlugCollisions.push({ key: g.key, slug, distinctClauses: [...distinctClauses] });
      }
    }
  }

  const summary = {
    generatedAt: new Date().toISOString(),
    inputs: {
      candidatePoolFiles: [...new Set(pool.map(r => r.sourceFile))],
      candidatePoolSize: pool.length
    },
    totalActorWeaponGroups: groups.size,
    singletonGroups: singletonGroups.length,
    duplicateGroups: duplicateGroups.length,
    rowsInDuplicateGroups: duplicateGroups.reduce((s, g) => s + g.members.length, 0),
    duplicateGroupBreakdown: {
      identical: {
        groupCount: identicalGroups.length,
        rowCount: identicalGroups.reduce((s, g) => s + g.memberCount, 0),
        crossPackSpanningGroupCount: identicalGroups.filter(g => g.crossPackSpanning).length,
        note: 'Every member shares the exact printed clause text. Not a distinct-row case; almost always the same actor document duplicated across more than one source pack (packs/nonheroic.db and packs/npc.db both carry it). No available field distinguishes these from each other, so only one representative per group should be promoted; the promotion tool\'s duplicate-slug guard already refuses to promote more than one from the same batch.'
      },
      distinct: {
        groupCount: distinctGroups.length,
        rowCount: distinctGroups.reduce((s, g) => s + g.memberCount, 0),
        note: 'Every member has a distinct printed clause (different attack bonus and/or formula, e.g. a full-attack sequence). Every member of these groups is now independently promotable: match.rawClause gives each a unique marker and the disambiguated slug (bonus + formula suffix) gives each a unique candidate slug.'
      },
      mixed: {
        groupCount: mixedGroups.length,
        rowCount: mixedGroups.reduce((s, g) => s + g.memberCount, 0),
        note: 'Some members are identical-clause duplicates of each other and some are genuinely distinct rows, within the same actor+weapon-name group.'
      }
    },
    identityIntegrityCheck: {
      residualSlugCollisions: residualSlugCollisions.length,
      ok: residualSlugCollisions.length === 0
    },
    methodologyNote: 'Grouping mirrors tools/generate-nonheroic-damage-profile-candidates.mjs exactly (actorSlug + normalizeWeaponName(printedName)). This audit only covers the current "safe" Lane A candidate pool (safe-ordinary-weapon-candidate / safe-ordinary-weapon-with-delta); it does not re-derive Lane B buckets or already-covered exclusions, which remain the responsibility of tools/audit-nonheroic-lane-b-remainder.mjs.'
  };

  const output = {
    summary,
    residualSlugCollisions,
    sampleGroups: {
      identical: identicalGroups.slice(0, 10),
      distinct: distinctGroups.slice(0, 10),
      mixed: mixedGroups.slice(0, 10)
    }
  };

  writeText(OUTPUT_JSON, JSON.stringify(output, null, 2) + '\n');
  writeText(OUTPUT_MD, renderMarkdown(output));

  console.log(JSON.stringify(summary, null, 2));
}

function renderMarkdown(output) {
  const { summary, sampleGroups, residualSlugCollisions } = output;
  const lines = [];
  lines.push('# Nonheroic Lane A Candidate Identity Audit (generated)');
  lines.push('');
  lines.push(`Generated: ${summary.generatedAt}`);
  lines.push('');
  lines.push('Read-only audit of the current staged Lane A "safe" candidate pool');
  lines.push('(`data/nonheroic/generated/nonheroic-weapon-damage-candidates.*.json`),');
  lines.push('generated after the match.rawClause / disambiguated-slug identity fix in');
  lines.push('`tools/generate-nonheroic-damage-profile-candidates.mjs`. No profile was');
  lines.push('promoted, no weapon item was created, and no pack/runtime file was');
  lines.push('modified in producing this report.');
  lines.push('');
  lines.push('## Summary');
  lines.push('');
  lines.push(`- Candidate pool files: ${summary.inputs.candidatePoolFiles.join(', ')}`);
  lines.push(`- Candidate pool size (safe Lane A rows): ${summary.inputs.candidatePoolSize}`);
  lines.push(`- Distinct actor+weapon-name groups: ${summary.totalActorWeaponGroups}`);
  lines.push(`- Singleton groups (1 row, no identity ambiguity): ${summary.singletonGroups}`);
  lines.push(`- Duplicate groups (>1 row sharing actor+weapon-name): ${summary.duplicateGroups}`);
  lines.push(`- Rows inside duplicate groups: ${summary.rowsInDuplicateGroups}`);
  lines.push('');
  lines.push('### Duplicate group breakdown');
  lines.push('');
  lines.push(`- **Identical** (same printed clause repeated, almost always a cross-pack document duplicate): ${summary.duplicateGroupBreakdown.identical.groupCount} groups / ${summary.duplicateGroupBreakdown.identical.rowCount} rows (${summary.duplicateGroupBreakdown.identical.crossPackSpanningGroupCount} of those groups span both nonheroic.db and npc.db). ${summary.duplicateGroupBreakdown.identical.note}`);
  lines.push(`- **Distinct** (genuinely different attack rows, e.g. a full-attack sequence): ${summary.duplicateGroupBreakdown.distinct.groupCount} groups / ${summary.duplicateGroupBreakdown.distinct.rowCount} rows. ${summary.duplicateGroupBreakdown.distinct.note}`);
  lines.push(`- **Mixed** (both patterns within the same group): ${summary.duplicateGroupBreakdown.mixed.groupCount} groups / ${summary.duplicateGroupBreakdown.mixed.rowCount} rows. ${summary.duplicateGroupBreakdown.mixed.note}`);
  lines.push('');
  lines.push('## Identity integrity check');
  lines.push('');
  lines.push(`- Rows with genuinely different printed content (different rawClause) sharing the same candidate slug: ${summary.identityIntegrityCheck.residualSlugCollisions}`);
  lines.push(`- Overall: ${summary.identityIntegrityCheck.ok ? 'OK -- every genuinely distinct printed row now has its own unique slug and marker.' : 'ATTENTION -- see residualSlugCollisions in the JSON report; these are the rows the disambiguated slug (bonus + formula suffix) still could not tell apart (e.g. same actor, same weapon, same printed bonus text and formula token but a different annotation/suffix) and need either manual review or a further disambiguator.'}`);
  lines.push('');
  if (residualSlugCollisions.length) {
    lines.push('### Residual slug collisions (need manual review)');
    lines.push('');
    for (const c of residualSlugCollisions) lines.push(`- \`${c.slug}\` in group \`${c.key}\`: ${JSON.stringify(c.distinctClauses)}`);
    lines.push('');
  }
  lines.push('> `identical` groups are expected and out of scope for this check: no');
  lines.push('> available field distinguishes two literally-identical printed rows from');
  lines.push('> each other, so their shared marker/slug is correct, not a bug. The');
  lines.push('> promotion tool\'s existing duplicate-slug guard already prevents');
  lines.push('> promoting more than one representative from the same batch.');
  lines.push('');

  const sampleSection = (title, groups) => {
    lines.push(`## ${title} (showing up to 10 of ${groups === sampleGroups.identical ? summary.duplicateGroupBreakdown.identical.groupCount : groups === sampleGroups.distinct ? summary.duplicateGroupBreakdown.distinct.groupCount : summary.duplicateGroupBreakdown.mixed.groupCount})`);
    lines.push('');
    if (!groups.length) {
      lines.push('_None._');
      lines.push('');
      return;
    }
    for (const g of groups) {
      lines.push(`- **${g.sampleActorName} / ${g.sampleWeaponName}** (\`${g.key}\`) — ${g.memberCount} rows, cross-pack: ${g.crossPackSpanning}`);
      for (const m of g.members) {
        lines.push(`  - \`${m.slug}\` — "${m.rawClause}" — ${m.sourceFile}`);
      }
    }
    lines.push('');
  };
  sampleSection('Sample: identical (cross-pack/document duplicate) groups', sampleGroups.identical);
  sampleSection('Sample: distinct (genuine multi-row) groups', sampleGroups.distinct);
  sampleSection('Sample: mixed groups', sampleGroups.mixed);

  return lines.join('\n');
}

main();
