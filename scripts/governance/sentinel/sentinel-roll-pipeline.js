/**
 * sentinel-roll-pipeline.js — Monitor roll execution routing through SWSE engine
 *
 * Validates:
 * - Rolls use async evaluation (Roll.evaluate({ async: true }))
 * - Chat cards include SWSE governance flags
 * - Rolls route through SWSEChat (not direct toMessage)
 * - Metadata present (source, modifiers, etc.)
 *
 * Passive sampling (first 50 rolls, then 1-in-10).
 */

import { SentinelEngine } from "/systems/foundryvtt-swse/scripts/governance/sentinel/sentinel-core.js";

export class SentinelRollPipeline {
  static rollCount = 0;
  static validatedCount = 0;
  static issueCount = 0;

  static init() {
    SentinelEngine.registerLayer("roll-pipeline", {
      enabled: true,
      readOnly: true,
      description: "Roll execution routing and governance validation"
    });

    // Hook into chat message creation (where rolls appear)
    Hooks.on("createChatMessage", (message) => this.monitorChatMessage(message));

    console.log("[SWSE Sentinel] Roll-Pipeline layer initialized");
  }

  /**
   * Monitor chat message for roll compliance
   */
  static monitorChatMessage(message) {
    if (!message || !message.rolls || message.rolls.length === 0) {
      return; // Not a roll message
    }

    this.rollCount++;

    // Sample: first 50 rolls, then 1-in-10
    if (this.validatedCount < 50) {
      // Always validate first 50
      this._validateRoll(message);
      this.validatedCount++;
    } else if (this.rollCount % 10 === 0) {
      // Sample 1-in-10 thereafter
      this._validateRoll(message);
      this.validatedCount++;
    }
  }

  /**
   * Validate a single roll message
   */
  static _validateRoll(message) {
    const issues = [];

    // Check 1: Roll async evaluation
    for (const roll of message.rolls) {
      // Roll should have async flag or promise resolved
      if (roll._evaluated === false) {
        issues.push({
          type: "NOT_EVALUATED",
          message: "Roll not evaluated"
        });
      }
    }

    // Check 2: SWSE governance flags
    const hasSwseFlags = message.flags?.swse !== undefined;
    const hasRollSource = message.flags?.swse?.source !== undefined;

    if (!hasSwseFlags) {
      issues.push({
        type: "MISSING_SWSE_FLAGS",
        message: "No SWSE governance flags present"
      });
    }

    if (!hasRollSource) {
      issues.push({
        type: "MISSING_ROLL_SOURCE",
        message: "Roll source not documented"
      });
    }

    // Check 3: Metadata presence
    const hasMetadata = message.flags?.swse?.metadata !== undefined;
    if (!hasMetadata) {
      issues.push({
        type: "MISSING_METADATA",
        message: "Roll metadata missing (modifiers, context, etc.)"
      });
    }

    // Check 4: Detect direct toMessage() calls (should use SWSEChat instead)
    const content = message.content || "";
    if (content.includes("<!-- Generated by Roll -->")) {
      issues.push({
        type: "DIRECT_TO_MESSAGE",
        message: "Detected direct roll.toMessage() call (should use SWSEChat)"
      });
    }

    // Report issues if found
    if (issues.length > 0) {
      this.issueCount += issues.length;

      SentinelEngine.report({
        aggregationKey: "sentinel-roll-pipeline-issues",
        severity: issues.some(i => i.type === "DIRECT_TO_MESSAGE") ? "WARN" : "INFO",
        layer: "roll-pipeline",
        title: `Roll governance issue detected (${issues.length} problems)`,
        details: {
          rollCount: this.rollCount,
          validatedCount: this.validatedCount,
          issues: issues.slice(0, 3), // Limit to first 3
          actor: message.author?.name,
          source: message.flags?.swse?.source || "unknown"
        },
        timestamp: Date.now()
      });
    }

    // Periodic success report
    if (this.validatedCount % 50 === 0 && this.validatedCount > 0) {
      SentinelEngine.report({
        aggregationKey: "sentinel-roll-pipeline-sample",
        severity: "INFO",
        layer: "roll-pipeline",
        title: `Roll validation sample: ${this.validatedCount} rolls checked`,
        details: {
          validated: this.validatedCount,
          issuesFound: this.issueCount,
          complianceRate: `${Math.round((1 - this.issueCount / this.validatedCount) * 100)}%`,
          totalRolls: this.rollCount
        },
        timestamp: Date.now(),
        devOnly: true
      });
    }
  }
}

// Auto-init
if (typeof Hooks !== "undefined") {
  Hooks.once("ready", () => {
    if (game.settings.get?.("foundryvtt-swse", "sentinelRollPipeline") ?? true) {
      SentinelRollPipeline.init();
    }
  });
}
